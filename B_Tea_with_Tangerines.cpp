int ceil(int a, int b) {
    return (a+b-1)/b;
}
void solve() {
    int n;
    cin>>n;
    vector<int> arr(n);
    for(int i=0;i<n;i++) {
        cin>>arr[i];
    }
    sort(all(arr));
    int ans = 0;
    int res = 2*arr[0];
    res--;
    for(int i=1;i<n;i++) {
        ans += (ceil(arr[i], res) -1);
    }
    cout<<ans<<endl;
}

