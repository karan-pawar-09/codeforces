void solve() {
    int l, r;
    cin>>l>>r;
    int ans = 0;
    for(int i = ceil(sqrt(l)); i*i <= 1^12 ; i++) {
        ans += (int) sqrt(i);
    }
    cout<<ans<<endl;
}

