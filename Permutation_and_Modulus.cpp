void solve() {
    int n;
    cin>>n;
    vector<int> arr(n);
    for(int i=0;i<n;i++) {
        arr[i] = i+2;
    }
    arr[n-1] = 1;
    for(auto x: arr) {
        cout<<x<<" ";
    }
    cout<<endl;
}

